export function LegalInfoScreen({ navigation, route }: Props) {
  const insets = useSafeAreaInsets();
  const initial = route.params?.section ?? 'privacy';
  const [section, setSection] = useState<SectionKey>(initial);

  useEffect(() => {
    if (route.params?.section) {
      setSection(route.params.section);
    }
  }, [route.params?.section]);

  const body = useMemo(() => CONTENT[section], [section]);

  return (
    <View style={[styles.root, { paddingTop: insets.top }]} testID="screen-legal">
      <View style={[styles.decor, { pointerEvents: 'none' }]}>
        <GridBackground opacity={0.04} />
        <AnimatedNeonBackground intensity="menu" />
      </View>

      <ScreenTopBar
        title="LEGAL (DRAFT)"
        accent={ACCENT}
        onBack={() => {
          if (navigation.canGoBack()) navigation.goBack();
          else navigation.navigate('Settings');
        }}
      />

      <View style={styles.tabs}>
        {SECTIONS.map(({ key, label }) => {
          const on = key === section;
          return (
            <Pressable
              key={key}
              testID={`legal-tab-${key}`}
              onPress={() => setSection(key)}
              style={[
                styles.tab,
                on && {
                  borderColor: ACCENT,
                  backgroundColor: withAlpha(ACCENT, 0.16),
                },
                on ? neonGlow(ACCENT, 3) : null,
              ]}
            >
              <Text style={[styles.tabText, on && { color: colors.white }]}>{label}</Text>
            </Pressable>
          );
        })}
      </View>

      <ScrollView
        contentContainerStyle={styles.scroll}
        showsVerticalScrollIndicator={false}
      >
        <View style={[styles.badge, neonGlow(colors.orange, 4)]}>
          <Text style={styles.badgeText}>DRAFT — NOT FINAL LEGAL COPY</Text>
        </View>
        <Text style={styles.body}>{body}</Text>
      </ScrollView>
    </View>
  );
}

const styles = StyleSheet.create({
  root: { flex: 1, backgroundColor: colors.background },
  decor: { ...StyleSheet.absoluteFill },
  tabs: {
    flexDirection: 'row',
    flexWrap: 'wrap',
    gap: 8,
    paddingHorizontal: 16,
    paddingBottom: 8,
  },
  tab: {
    borderWidth: 1,
    borderColor: withAlpha(colors.muted, 0.3),
    borderRadius: radii.compact,
    paddingHorizontal: 10,
    paddingVertical: 8,
    backgroundColor: colors.card,
  },
  tabText: {
    fontFamily: fontFamilies.rajdhaniSemiBold,
    fontSize: 13,
    color: colors.muted,
  },
  scroll: { padding: 16, paddingBottom: 40, gap: 12 },
  badge: {
    alignSelf: 'flex-start',
    borderWidth: 1,
    borderColor: withAlpha(colors.orange, 0.5),
    backgroundColor: withAlpha(colors.orange, 0.12),
    borderRadius: radii.compact,
    paddingHorizontal: 10,
    paddingVertical: 6,
  },
  badgeText: {
    fontFamily: fontFamilies.orbitronBold,
    fontSize: 10,
    letterSpacing: 1,
    color: colors.orange,
  },
  body: {
    fontFamily: fontFamilies.rajdhaniSemiBold,
    fontSize: 14,
    lineHeight: 22,
    color: colors.white,
  },
});
